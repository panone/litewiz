#include "file_cluster.h"
#include "variant.h"

/*******************************************************************************
*******************************************************************************/
Variant::Variant
(
    QString                const & name,
    QString                const & stem,
    QList< FileCluster * > const & collection
) :
    FileCluster( name, stem, collection )
{
    reference = false;
}

/*******************************************************************************
*******************************************************************************/
void Variant::setReference
(
    bool const reference
)
{
    this->reference = reference;
}

/*******************************************************************************
*******************************************************************************/
bool Variant::isReference
(
    void
)
    const
{
    return reference;
}

