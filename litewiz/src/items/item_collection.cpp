/*******************************************************************************
*******************************************************************************/

#include <QList>
#include "classifier.h"
#include "item.h"
#include "item_collection.h"

/*******************************************************************************
*******************************************************************************/
ItemCollection::~ItemCollection
(
    void
)
{
    qDeleteAll( items );
}

/*******************************************************************************
*******************************************************************************/
Item * ItemCollection::addItem
(
    ItemInfo const & itemInfo
)
{
    Item * item = new Item( itemInfo.name, itemInfo.stem );

    items.append( item );

    return item;
}

/*******************************************************************************
*******************************************************************************/
void ItemCollection::clear
(
    void
)
{
    qDeleteAll( items );

    items.clear();
}

