#include <QList>
#include "item.h"
#include "item_collection.h"

/*******************************************************************************
*******************************************************************************/
ItemCollection::~ItemCollection
(
    void
)
{
    qDeleteAll( items );
}

/*******************************************************************************
*******************************************************************************/
Item * ItemCollection::addItem
(
    void
)
{
    Item * item = new Item( "item", "item_stem" );

    items.append( item );

    return item;
}

