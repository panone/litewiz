/*******************************************************************************
*******************************************************************************/

#include <QString>
#include "item.h"

/*******************************************************************************
*******************************************************************************/
Item::Item
(
    QString const & name,
    QString const & stem
) :
    name( name ),
    stem( stem )
{
    excluded = false;
}

/*******************************************************************************
*******************************************************************************/
QString Item::getName
(
    void
)
    const
{
    return name;
}

/*******************************************************************************
*******************************************************************************/
QString Item::getStem
(
    void
)
    const
{
    return stem;
}

/*******************************************************************************
*******************************************************************************/
void Item::exclude
(
    bool const exclude
)
{
    excluded = exclude;
}

/*******************************************************************************
*******************************************************************************/
bool Item::isExcluded
(
    void
)
    const
{
    return excluded;
}

