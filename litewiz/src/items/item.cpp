#include <QString>
#include "item.h"

/*******************************************************************************
*******************************************************************************/
Item::Item
(
    QString const & name,
    QString const & stem
) :
    name( name ),
    stem( stem )
{
}

/*******************************************************************************
*******************************************************************************/
QString Item::getName
(
    void
)
    const
{
    return name;
}

/*******************************************************************************
*******************************************************************************/
QString Item::getStem
(
    void
)
    const
{
    return stem;
}

