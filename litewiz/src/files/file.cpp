/*******************************************************************************
*******************************************************************************/

#include <QFileInfo>
#include <QString>
#include "file.h"

/*******************************************************************************
*******************************************************************************/
File::File
(
    QString const & fileName
) :
    info( fileName )
{
}

/*******************************************************************************
*******************************************************************************/
QString File::getPath
(
    void
)
    const
{
    return info.absolutePath();
}

/*******************************************************************************
*******************************************************************************/
QString File::getName
(
    void
)
    const
{
    return info.fileName();
}

/*******************************************************************************
*******************************************************************************/
QString File::getItem
(
    void
)
    const
{
    return "item";
}

/*******************************************************************************
*******************************************************************************/
QString File::getVariant
(
    void
)
    const
{
    return "variant";
}

