/*******************************************************************************
*******************************************************************************/

#include <QString>
#include "file_cluster.h"

/*******************************************************************************
*******************************************************************************/
FileCluster::FileCluster
(
    QString const & name,
    QString const & stem
) :
    name( name ),
    stem( stem )
{
    excluded = false;
}

/*******************************************************************************
*******************************************************************************/
QString FileCluster::getName
(
    void
)
    const
{
    return name;
}

/*******************************************************************************
*******************************************************************************/
QString FileCluster::getStem
(
    void
)
    const
{
    return stem;
}

/*******************************************************************************
*******************************************************************************/
void FileCluster::exclude
(
    bool const exclude
)
{
    excluded = exclude;
}

/*******************************************************************************
*******************************************************************************/
bool FileCluster::isExcluded
(
    void
)
    const
{
    return excluded;
}

