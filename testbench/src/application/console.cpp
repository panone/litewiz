/*******************************************************************************
*******************************************************************************/

#include <QTextStream>
#include "console.h"

/******************************************************************************/

Console * Console::instance = 0;

/*******************************************************************************
*******************************************************************************/
QTextStream & Console::output
(
    void
)
{
    return instance->outputStream;
}

/*******************************************************************************
*******************************************************************************/
QTextStream & Console::input
(
    void
)
{
    return instance->inputStream;
}

/*******************************************************************************
*******************************************************************************/
Console::Console
(
    void
) :
    outputStream( stdout ),
    inputStream( stdin )
{
    instance = this;
}

/*******************************************************************************
*******************************************************************************/
Console::~Console
(
    void
)
{
    outputStream.flush();
}

/******************************************************************************/
